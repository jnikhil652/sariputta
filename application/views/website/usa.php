        <h4>Associate at Community College</h4>
        <ul>
          <li>Many students choose to study at a community college in order to complete the first two years of prerequisite courses. They will earn an Associate of Arts (AA) transfer degree and then transfer to a four-year university or college.</li>
        </ul>
        <h4>Undergraduate at College / University</h4>
        <ul>
          <li>It takes four years to earn a bachelor’s degree. First two years of study requires taking a wide variety of classes in different subjects, commonly known as prerequisite courses: literature,science, the social sciences, the arts, history, and so forth.</li>
          <li>Bachelor’s degree: 4 years; Year 1 = Freshman, Year 2 = Sophomore, Year 3 = Junior, and Year 4 = Senior.</li>
          <li>The two types of bachelor’s degrees typically offered by these institutions are BA (Bachelor of Arts) or BS (Bachelor of Science). Students pick a “major” at a certain point as they are working toward such degrees. A major is the specific field of study in which degree is focused and consists of the courses within a degree program that most interest the student.</li>
        </ul>
        <h4>Graduate at College / University</h4>
        <ul>
          <li>A graduate program is usually a division of a university or college.</li>
          <li>Graduate programs in pursuit of a master’s degree typically take one to two years to complete.</li>
          <li>The majority of a master’s program is spent in classroom study and a graduate student must prepare a long research paper called a master’s thesis or complete a master’s project.</li>
        </ul>
        <h4>Doctorate at University</h4>
        <ul>
          <li>Most graduate schools requires minimum master’s degree as the first step towards earning a PhD (doctorate). It may take three years or more to earn a PhD degree.</li>
          <li>Doctoral degrees can be academic or professional in nature. To receive their degrees, students need to pass an examination and complete an original piece of research and submit a dissertation.</li>
        </ul>
