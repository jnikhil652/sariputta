        <h5>Hauptschule [ Duration : 5 years ]</h5>
        <p>The main objective is to prepare students for their entry into the world of work. Once students have obtained their Hauptschulabschluss (leaving certificate) at the age of 15-16, they can go into vocational training, start entry-level work in the public sector, or attend a full-time vocational school</p>
        <h5>Realschule [ Duration : 6 years ]</h5>
        <p>It gives them a broader general education and expects them to show greater independence. Students are given more vocationally oriented education.
At the end of class 10 they are given different options: in-company vocational training, work in the public sector at entry and executive level, or further school-level education at secondary level II or at a Fachhochschule.</p>
        <h5>Gymnasium [ Duration : 8 or 9 years ]</h5>
        <p>This is designed to provide students with an education which will enable them to study at a German university or equivalent. Students at secondary level II (the last two or three years at the Gymnasium) select two or three specialist subjects.</p>
        <h5>Gesamtschule [ Duration : 6 years ]</h5>
        <p>The Gesamtschule (comprehensive school) combines elements from the Hauptschule, the Realschule and the Gymnasium.</p>
